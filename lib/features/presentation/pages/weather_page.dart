  void _addCity(String cityName) {
    if (cityName.trim().isEmpty) return;
    debugPrint("fetch city: $cityName");

    context.read<WeatherBloc>().add(
      FetchWeatherEvent(location: cityName.trim()),
    );
    _cityController.clear();
  }

  List<Autogenerated> _getCitiesFromState(WeatherState state) {
    return state.cities;
  }

  // Helper: Build address string from area data
  String _buildAddress(dynamic area) {
    final parts = <String>[];
    if (area.areaName?.isNotEmpty == true) {
      parts.add(area.areaName!.first.value ?? '');
    }
    if (area.region?.isNotEmpty == true) {
      parts.add(area.region!.first.value ?? '');
    }
    if (area.country?.isNotEmpty == true) {
      parts.add(area.country!.first.value ?? '');
    }
    return parts.isEmpty ? 'Current Location' : parts.join(', ');
  }

  Map<String, dynamic> _extractCurrentWeather(
    Autogenerated data,
    dynamic area,
  ) {
    final condition = data.currentCondition?.isNotEmpty == true
        ? data.currentCondition!.first
        : null;

    return {
      'address': _buildAddress(area),
      'temperature': condition?.tempC != null ? '${condition!.tempC}°C' : 'N/A',
      'humidity': condition?.humidity != null
          ? '${condition!.humidity}%'
          : 'N/A',
      'windSpeed': condition?.windspeedKmph != null
          ? '${condition!.windspeedKmph} km/h'
          : 'N/A',
      'weatherDesc': condition?.weatherDesc?.isNotEmpty == true
          ? condition!.weatherDesc!.first.value ?? 'N/A'
          : 'N/A',
      'feelsLike': condition?.feelsLikeC != null
          ? '${condition!.feelsLikeC}°C'
          : 'N/A',
      'pressure': condition?.pressure != null
          ? '${condition!.pressure} mb'
          : 'N/A',
      'uvIndex': condition?.uvIndex ?? 'N/A',
      'country': area.country?.isNotEmpty == true
          ? area.country!.first.value ?? ''
          : '',
      'region': area.region?.isNotEmpty == true
          ? area.region!.first.value ?? ''
          : '',
      'latitude': area.latitude ?? '',
      'longitude': area.longitude ?? '',
      'date': 'Today',
      'type': 'current',
    };
  }

  // Helper: Extract forecast weather data
  Map<String, dynamic> _extractForecastWeather(
    dynamic weather,
    String baseAddress,
    Autogenerated data,
  ) {
    final nearestArea = data.nearestArea?.isNotEmpty == true
        ? data.nearestArea!.first
        : null;

    return {
      'address': baseAddress.isNotEmpty ? baseAddress : 'Forecast',
      'temperature': weather.avgtempC != null ? '${weather.avgtempC}°C' : 'N/A',
      'humidity': 'N/A',
      'windSpeed': 'N/A',
      'weatherDesc': 'Forecast',
      'country': nearestArea?.country?.isNotEmpty == true
          ? nearestArea!.country!.first.value ?? ''
          : '',
      'region': nearestArea?.region?.isNotEmpty == true
          ? nearestArea!.region!.first.value ?? ''
          : '',
      'date': weather.date ?? 'N/A',
      'maxTemp': weather.maxtempC ?? 'N/A',
      'minTemp': weather.mintempC ?? 'N/A',
      'avgtempC': weather.avgtempC ?? 'N/A',
      'avgtempF': weather.avgtempF ?? 'N/A',
      'sunHour': weather.sunHour ?? 'N/A',
      'uvIndex': weather.uvIndex ?? 'N/A',
      'type': 'forecast',
    };
  }

  List<Map<String, dynamic>> _buildWeatherList(List<Autogenerated> citiesList) {
    final weatherList = <Map<String, dynamic>>[];

    for (var data in citiesList) {
      // Process current weather
      if (data.nearestArea?.isNotEmpty == true) {
        for (var area in data.nearestArea!) {
          weatherList.add(_extractCurrentWeather(data, area));
        }
      }

      // Process forecast weather
      if (data.weather?.isNotEmpty == true) {
        final baseAddress =
            data.nearestArea?.isNotEmpty == true &&
                data.nearestArea!.first.areaName?.isNotEmpty == true
            ? data.nearestArea!.first.areaName!.first.value ?? ''
            : '';
        for (var weather in data.weather!) {
          weatherList.add(_extractForecastWeather(weather, baseAddress, data));
        }
      }
    }

    return weatherList;
  }

  // Helper: Filter weather list based on search query
  List<Map<String, dynamic>> _filterWeatherList(
    List<Map<String, dynamic>> weatherList,
    String searchQuery,
  ) {
    if (searchQuery.isEmpty) return weatherList;

    final query = searchQuery.toLowerCase();
    return weatherList.where((item) {
      final address = (item['address'] ?? '').toString().toLowerCase();
      final country = (item['country'] ?? '').toString().toLowerCase();
      final region = (item['region'] ?? '').toString().toLowerCase();
      final date = (item['date'] ?? '').toString().toLowerCase();

      return address.contains(query) ||
          country.contains(query) ||
          region.contains(query) ||
          date.contains(query);
    }).toList();
  }

  // Helper: Get city name from data for refresh
  String? _getCityName(Autogenerated city) {
    if (city.nearestArea?.isNotEmpty == true) {
      final area = city.nearestArea!.first;
      if (area.areaName?.isNotEmpty == true) {
        return area.areaName!.first.value;
      }
    }
    return null;
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Container(
        decoration: BoxDecoration(
          gradient: LinearGradient(
            begin: Alignment.topLeft,
            end: Alignment.bottomRight,
            colors: [Color(0xFF1E3C72), Color(0xFF2A5298), Color(0xFF7B68EE)],
          ),
        ),
        child: SafeArea(
          child: BlocBuilder<WeatherBloc, WeatherState>(
            builder: (context, state) {
              final citiesList = _getCitiesFromState(state);

              // Show skeleton loading when loading or when initial state with no cities
              if (state is WeatherLoading) {
                return Column(
                  children: [
                    _buildHeader(context, citiesList, state.searchQuery),
                    _buildSkeletonLoadingState(),
                  ],
                );
              }

              // Show skeleton on initial empty state instead of empty state
              if (state is WeatherInitial && citiesList.isEmpty) {
                return Column(
                  children: [
                    _buildHeader(context, citiesList, state.searchQuery),
                    _buildSkeletonLoadingState(),
                  ],
                );
              }

              // Error state
              if (state is WeatherError) {
                return _buildErrorState(context, state.message);
              }

              // Empty state with message
              if (citiesList.isEmpty) {
                return _buildEmptyState();
              }

              // Main content with weather list
              final weatherList = _buildWeatherList(citiesList);
              final filteredList = _filterWeatherList(
                weatherList,
                state.searchQuery,
              );

              return Column(
                children: [
                  _buildHeader(context, citiesList, state.searchQuery),
                  _buildWeatherListWidget(filteredList, weatherList),
                ],
              );
            },
          ),
        ),
      ),
    );
  }

