          child: BlocBuilder<WeatherBloc, WeatherState>(
            builder: (context, state) {
              final citiesList = _getCitiesFromState(state);

              if (state is WeatherInitial && citiesList.isEmpty) {
                return _buildInitialEmptyState(context);
              }

              // Loading state
              if (state is WeatherLoading) {
                return Column(
                  children: [
                    _buildHeader(context, citiesList, state.searchQuery),
                    _buildSkeletonLoadingState(),
                  ],
                );
              }

              // Error state
              if (state is WeatherError) {
                return _buildErrorState(context, state.message);
              }

              // Empty state with message
              if (citiesList.isEmpty) {
                return _buildEmptyState();
              }

              // Main content with weather list
              final weatherList = _buildWeatherList(citiesList);
              final filteredList = _filterWeatherList(
                weatherList,
                state.searchQuery,
              );

              return Column(
                children: [
                  _buildHeader(context, citiesList, state.searchQuery),
                  _buildWeatherListWidget(filteredList, weatherList),
                ],
              );
            },
          ),
        ),
      ),
    );
  }

