        shape: BoxShape.circle,
        boxShadow: [
          BoxShadow(
            color: Colors.blue.withOpacity(0.3),
            blurRadius: 10,
            offset: Offset(0, 5),
          ),
        ],
      ),
      child: Icon(
        isCurrent ? Icons.wb_sunny : Icons.calendar_today,
        color: Colors.white,
        size: 35,
      ),
    );
  }

  // UI Widget: Location info
  Widget _buildLocationInfo(Map<String, dynamic> item, bool isCurrent) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          children: [
            Icon(Icons.location_on, color: Colors.red, size: 20),
            SizedBox(width: 5),
            Expanded(
              child: Text(
                item['address'] ?? 'Unknown',
                style: TextStyle(
                  fontSize: 18,
                  fontWeight: FontWeight.bold,
                  color: Color(0xFF1E3C72),
                ),
                maxLines: 1,
                overflow: TextOverflow.ellipsis,
              ),
            ),
          ],
        ),
        if (item['date'] != null) ...[
          SizedBox(height: 5),
          Text(
            item['date'] ?? '',
            style: TextStyle(fontSize: 14, color: Colors.grey[600]),
          ),
        ],
        if (isCurrent && item['weatherDesc'] != null) ...[
          SizedBox(height: 8),
          Container(
            padding: EdgeInsets.symmetric(horizontal: 10, vertical: 4),
            decoration: BoxDecoration(
              color: Color(0xFF667EEA).withOpacity(0.1),
              borderRadius: BorderRadius.circular(12),
            ),
            child: Text(
              item['weatherDesc'] ?? '',
              style: TextStyle(
                fontSize: 12,
                color: Color(0xFF667EEA),
                fontWeight: FontWeight.w500,
              ),
            ),
          ),
        ],
      ],
    );
  }

  // UI Widget: Temperature info
  Widget _buildTemperatureInfo(Map<String, dynamic> item, bool isCurrent) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.end,
      children: [
        Text(
          item['temperature'] ?? 'N/A',
          style: TextStyle(
            fontSize: 28,
            fontWeight: FontWeight.bold,
            color: Color(0xFFFF6B6B),
          ),
        ),
        if (isCurrent) ...[
          SizedBox(height: 5),
          _buildInfoRow(
            Icons.water_drop,
            item['humidity'] ?? 'N/A',
            Colors.blue,
          ),
          SizedBox(height: 3),
          _buildInfoRow(Icons.air, item['windSpeed'] ?? 'N/A', Colors.grey),
          if (item['feelsLike'] != null && item['feelsLike'] != 'N/A') ...[
            SizedBox(height: 3),
            _buildInfoRow(
              Icons.thermostat,
              'Feels: ${item['feelsLike']}',
              Colors.orange,
            ),
          ],
        ] else ...[
          SizedBox(height: 5),
          Text(
            'Max: ${item['maxTemp']}°C',
            style: TextStyle(
              fontSize: 12,
              color: Colors.orange,
              fontWeight: FontWeight.w600,
            ),
          ),
          Text(
            'Min: ${item['minTemp']}°C',
            style: TextStyle(
              fontSize: 12,
              color: Colors.blue,
              fontWeight: FontWeight.w600,
            ),
          ),
          if (item['uvIndex'] != null && item['uvIndex'] != 'N/A')
            Padding(
              padding: EdgeInsets.only(top: 3),
              child: Text(
                'UV: ${item['uvIndex']}',
                style: TextStyle(
                  fontSize: 11,
                  color: Colors.purple,
                  fontWeight: FontWeight.w500,
                ),
              ),
            ),
        ],
      ],
    );
  }

  // UI Widget: Info row helper
  Widget _buildInfoRow(IconData icon, String text, Color color) {
    return Row(
      children: [
        Icon(icon, size: 14, color: color),
        SizedBox(width: 4),
        Text(text, style: TextStyle(fontSize: 12, color: Colors.grey[600])),
      ],
    );
  }
