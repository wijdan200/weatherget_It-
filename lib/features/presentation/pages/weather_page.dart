  // UI Widget: Weather list
  Widget _buildWeatherListWidget(
    List<Map<String, dynamic>> filteredList,
    List<Map<String, dynamic>> weatherList,
  ) {
    if (filteredList.isEmpty) {
      return Expanded(
        child: Center(
          child: Column(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              Icon(Icons.search_off, size: 80, color: Colors.white54),
              SizedBox(height: 20),
              Text(
                weatherList.isEmpty
                    ? 'No weather data available'
                    : 'No results found',
                style: GoogleFonts.getFont(
                  'Lato',
                  fontSize: 18,
                  color: Colors.white70,
                ),
              ),
            ],
          ),
        ),
      );
    }

    return Expanded(
      child: ListView.builder(
        itemCount: filteredList.length,
        padding: EdgeInsets.symmetric(horizontal: 20),
        physics: BouncingScrollPhysics(),
        itemBuilder: (context, index) {
          final item = filteredList[index];
          final isCurrent = item['type'] == 'current';

          return TweenAnimationBuilder<double>(
            tween: Tween(begin: 0.0, end: 1.0),
            duration: Duration(milliseconds: 800 + (index * 800)),
            builder: (context, value, child) {
              return Opacity(
                opacity: value,
                child: Transform.translate(
                  offset: Offset(-50 * (1 - value), 0),
                  child: child,
                ),
              );
            },
            child: BlocBuilder<WeatherBloc, WeatherState>(
              builder: (context, state) {
                // Determine if this specific item is hovered based on its index
                // Note: We use a different range of indices or just reuse the same map key mechanism.
                // Since skeleton list and main list aren't shown together, reusing index is fine.
                // However, index 0 in skeleton means different UI than index 0 in main list.
                // But conceptually 'index 0 is hovered' is valid for the visible list.
                final isHovered = state.hoverStates[index] ?? false;

                return MouseRegion(
                  onEnter: (_) => context.read<WeatherBloc>().add(
                    SkeletonHoverEvent(index, true),
                  ),
                  onExit: (_) => context.read<WeatherBloc>().add(
                    SkeletonHoverEvent(index, false),
                  ),
                  child: AnimatedContainer(
                    duration: const Duration(milliseconds: 800),
                    transform: isHovered
                        ? (Matrix4.identity()..scale(1.02))
                        : Matrix4.identity(),
                    child: _buildWeatherCard(item, isCurrent),
                  ),
                );
              },
            ),
          );
        },
      ),
    );
  }

  // UI Widget: Weather card item
  Widget _buildWeatherCard(Map<String, dynamic> item, bool isCurrent) {
    return Container(
      margin: EdgeInsets.only(bottom: 15),
      decoration: BoxDecoration(
        gradient: LinearGradient(
          begin: Alignment.topLeft,
          end: Alignment.bottomRight,
          colors: isCurrent
              ? [const Color.fromARGB(255, 225, 129, 129), Colors.blue.shade50]
              : [
                  Colors.white.withOpacity(0.95),
                  Colors.white.withOpacity(0.85),
                ],
        ),
        borderRadius: BorderRadius.circular(20),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withOpacity(0.1),
            blurRadius: 15,
            offset: Offset(0, 5),
          ),
        ],
      ),
      child: Padding(
        padding: EdgeInsets.all(20),
        child: Row(
          children: [
            _buildWeatherIcon(isCurrent),
            SizedBox(width: 20),
            Expanded(child: _buildLocationInfo(item, isCurrent)),
            _buildTemperatureInfo(item, isCurrent),
          ],
        ),
      ),
    );
  }

  // UI Widget: Weather icon
  Widget _buildWeatherIcon(bool isCurrent) {
    return Container(
      width: 70,
      height: 70,
      decoration: BoxDecoration(
        gradient: LinearGradient(
          colors: [Color(0xFF667EEA), Color(0xFF764BA2)],
        ),
        shape: BoxShape.circle,
        boxShadow: [
          BoxShadow(
            color: Colors.blue.withOpacity(0.3),
            blurRadius: 10,
            offset: Offset(0, 5),
          ),
        ],
      ),
      child: Icon(
        isCurrent ? Icons.wb_sunny : Icons.calendar_today,
        color: Colors.white,
        size: 35,
      ),
    );
  }

  // UI Widget: Location info
  Widget _buildLocationInfo(Map<String, dynamic> item, bool isCurrent) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          children: [
            Icon(Icons.location_on, color: Colors.red, size: 20),
            SizedBox(width: 5),
            Expanded(
              child: Text(
                item['address'] ?? 'Unknown',
                style: GoogleFonts.getFont(
                  'Lato',
                  fontSize: 18,
                  fontWeight: FontWeight.bold,
                  color: Color(0xFF1E3C72),
                ),
                maxLines: 1,
                overflow: TextOverflow.ellipsis,
              ),
            ),
          ],
        ),
        if (item['date'] != null) ...[
          SizedBox(height: 5),
          Text(
            item['date'] ?? '',
            style: GoogleFonts.getFont(
              'Lato',
              fontSize: 14,
              color: Colors.grey[600],
            ),
          ),
        ],
        if (isCurrent && item['weatherDesc'] != null) ...[
          SizedBox(height: 8),
          Container(
            padding: EdgeInsets.symmetric(horizontal: 10, vertical: 4),
            decoration: BoxDecoration(
              color: Color(0xFF667EEA).withOpacity(0.1),
              borderRadius: BorderRadius.circular(12),
            ),
            child: Text(
              item['weatherDesc'] ?? '',
              style: GoogleFonts.getFont(
                'Lato',
                fontSize: 12,
                color: Color(0xFF667EEA),
                fontWeight: FontWeight.w500,
              ),
            ),
          ),
        ],
      ],
    );
  }

  // UI Widget: Temperature info
  Widget _buildTemperatureInfo(Map<String, dynamic> item, bool isCurrent) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.end,
      children: [
        Text(
          item['temperature'] ?? 'N/A',
          style: GoogleFonts.getFont(
            'Lato',
            fontSize: 28,
            fontWeight: FontWeight.bold,
            color: Color(0xFFFF6B6B),
          ),
        ),
        if (isCurrent) ...[
          SizedBox(height: 5),
          _buildInfoRow(
            Icons.water_drop,
            item['humidity'] ?? 'N/A',
            Colors.blue,
          ),
          SizedBox(height: 3),
          _buildInfoRow(Icons.air, item['windSpeed'] ?? 'N/A', Colors.grey),
          if (item['feelsLike'] != null && item['feelsLike'] != 'N/A') ...[
            SizedBox(height: 3),
            _buildInfoRow(
              Icons.thermostat,
              'Feels: ${item['feelsLike']}',
              Colors.orange,
            ),
          ],
        ] else ...[
          SizedBox(height: 5),
          Text(
            'Max: ${item['maxTemp']}°C',
            style: GoogleFonts.getFont(
              'Lato',
              fontSize: 12,
              color: Colors.orange,
              fontWeight: FontWeight.w600,
            ),
          ),
          Text(
            'Min: ${item['minTemp']}°C',
            style: GoogleFonts.getFont(
              'Lato',
              fontSize: 12,
              color: Colors.blue,
              fontWeight: FontWeight.w600,
            ),
          ),
          if (item['uvIndex'] != null && item['uvIndex'] != 'N/A')
            Padding(
              padding: EdgeInsets.only(top: 3),
              child: Text(
                'UV: ${item['uvIndex']}',
                style: GoogleFonts.getFont(
                  'Lato',
                  fontSize: 11,
                  color: Colors.purple,
                  fontWeight: FontWeight.w500,
                ),
              ),
            ),
        ],
      ],
    );
  }

  // UI Widget: Info row helper
  Widget _buildInfoRow(IconData icon, String text, Color color) {
    return Row(
      children: [
        Icon(icon, size: 14, color: color),
        SizedBox(width: 4),
        Text(
          text,
          style: GoogleFonts.getFont(
            'Lato',
            fontSize: 12,
            color: Colors.grey[600],
          ),
        ),
      ],
    );
  }
}
